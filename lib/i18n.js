export function useLang() {
  const [lang, setLang] = useState('ja');

  useEffect(() => {
    const saved = typeof window !== 'undefined' && localStorage.getItem('pl_lang');
    if (saved === 'ja' || saved === 'en') setLang(saved);
    else if (typeof navigator !== 'undefined' && !navigator.language.startsWith('ja')) setLang('en');
  }, []);

  const toggle = useCallback(() => {
    setLang((l) => {
      const next = l === 'ja' ? 'en' : 'ja';
      localStorage.setItem('pl_lang', next);
      return next;
    });
  }, []);

  const t = useCallback((key) => (dict[lang] && dict[lang][key]) || key, [lang]);
  return { lang, toggle, t };
}
